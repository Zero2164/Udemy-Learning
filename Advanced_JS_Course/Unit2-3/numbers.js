//Numbers Basics

//Regular order of operations applies

//Addition
3 + 4 // Prints: 7

//Subtraction
3 - 4 //Prints: -1

//Multiplication
3 * 4 //Prints: 12

//Division 
4 / 3 //Prints: 1.3

//Modulo!!
27 % 2 //Prints: 1

//Exponents
4 ** 3 //Prints: 64


//NaN value
0 / 0 //Prints: NaN

5 + NaN //Prints: NaN

//Infinity value
1 / 0 //Prints: Infinity

/////////////////////////////////////////////////
